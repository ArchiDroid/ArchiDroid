set -e

AD="/data/media/0/ArchiDroid"

touch /data/ARCHIDROID_DONT_REMOVE_ME
mkdir -p "$AD"
rm -f "$AD/INSTALL" "$AD/UPDATE" "$AD/FORCE"

for ARG in "$@"; do
	touch "$AD/$ARG"
done

